class Main {
  public static void main(String[] args) {
    double Sum;
    System.out.println("Print The Sum of 100*100");
    Sum = 100 * 100;
    System.out.println(Sum);
    System.out.println("Print the Sum of 1000+1000");
    Sum = 1000+1000;
    System.out.println(Sum);
    System.out.println("Subtract 1000-1000");
    Sum = 1000-1000;
    System.out.println(Sum);
    System.out.println("Divide 100/25");
    Sum = 100/25;
    System.out.println(Sum);
    System.out.println("Take the Square Root of 25");
    Sum = Math.sqrt(100);
    System.out.println(Sum);
  }
}